CREATE TABLE SERT(
   Id_Repas NUMBER(10),
   Id_Cuisinier NUMBER(10),
   Id_Restaurant NUMBER(10),
   PRIMARY KEY(Id_Repas, Id_Cuisinier, Id_Restaurant),
