   adresse VARCHAR2(50),
   courriel VARCHAR2(50),
   numTel NUMBER(10),
   adressePostale NUMBER(10),
   idG NUMBER(10) NOT NULL,
   idOT NUMBER(10),
   idCT NUMBER(10),
   idD NUMBER(10) NOT NULL,
   idT NUMBER(10) NOT NULL,
   Id_Rang NUMBER(10) NOT NULL,
