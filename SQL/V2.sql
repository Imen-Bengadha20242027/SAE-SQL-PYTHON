   Id_Machine NUMBER(10),
   Id_Historique NUMBER(10),
   PRIMARY KEY(Id_Machine, Id_Historique),
   FOREIGN KEY(Id_Machine) REFERENCES CERTIFICAT(Id_Machine),
   FOREIGN KEY(Id_Historique) REFERENCES HISTORIQUE(Id_Historique)
