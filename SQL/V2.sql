CREATE TABLE HEURTE(
   Id_Aliment NUMBER(10),
   Id_Croyance NUMBER(10),
   Id_OppositionConvictionPerso NUMBER(10),
   PRIMARY KEY(Id_Aliment, Id_Croyance, Id_OppositionConvictionPerso),
