CREATE TABLE REGISTRE(
   idRe NUMBER(10),
   statut VARCHAR2(50),
   date_ TIMESTAMP WITH TIME ZONE,
   idOT NUMBER(10) NOT NULL,
    NUMBER(10) NOT NULL,
   Id_Tenrac NUMBER(10) NOT NULL,
   Id_Machine NUMBER(10) NOT NULL,
   Id_Historique NUMBER(10) NOT NULL,
   PRIMARY KEY(Id_Registre),
   UNIQUE(Id_OrdreTenrac),
   FOREIGN KEY(Id_OrdreTenrac) REFERENCES ORDRE_TENRAC(Id_OrdreTenrac),
   FOREIGN KEY(Id_ClubTenrac) REFERENCES CLUB_TENRAC(Id_ClubTenrac),
