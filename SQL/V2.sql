   PRIMARY KEY(Id_Aliment, Id_Allergie),
   FOREIGN KEY(Id_Aliment) REFERENCES ALIMENT(Id_Aliment),
   FOREIGN KEY(Id_Allergie) REFERENCES ALLERGIE(Id_Allergie)
